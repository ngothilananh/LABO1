#Viết chương trình nhập vào 4 số nguyên a, b, c, d. 
#Tính trung bình cộng của 4 số trên và in kết quả ra màn hình. 
a=int(input("Nhập số nguyên a: "))
b=int(input("Nhập số nguyên b: "))
c=int(input("Nhập số nguyên c: "))
d=int(input("Nhập số nguyên d: "))
trungbinhcong= (a+b+c+d)/4
print("Trung bình cộng là: ", trungbinhcong)      